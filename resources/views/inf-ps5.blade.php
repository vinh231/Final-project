        <h1>PLAYSTATION 5</h1>
        <span>In the seven years since the introduction of the PlayStation 3, we've seen our gaming consoles transform into living-room hubs through constant evolution and software updates. Those updates weren't always smooth – though on PS3, they were always happening – but it's easy to see just how far the platform has come.

            <br>Meanwhile, the designers of the PlayStation 4 were taking notes and designing a console that, feature by feature, sought to address the failings of its predecessor. On November 15, 2013, the PlayStation 4 finally launched.</span>
        <video controls autoplay>
