        <h2>THE CONSOLE
           <br> HARDWARE & DESIGN</h2>
        <p>The PS4 is Sony's most attractively designed piece of hardware. It's a beautiful system, with a sharp, slightly angled profile accented by a light bar that acts as a console status indicator.

          <br>  On the back, PS4 has gone digital-only with HDMI/optical ports, and no analog audio or video outputs. We appreciate the internal power supply — it sounds like a small thing, but it's one less object to sit on the shelf next to or behind the PlayStation 4.</p>
        
          <img src="images/ps4.jpg" alt="">
        
    </div>

    <div class="row">
            <div class="about-col">
                <p>In the bad column, it's a collection of moderate to minor annoyances. The PS4 doesn't support the new 802.11ac wireless standard, instead relying on an 802.11b/g/n radio at 2.4 GHz — no 5 GHz support here either, all of which is disappointing to see on a consumer device in 2013. More annoyingly to many on staff, the PS4 doesn't include an IR port for universal remotes, nor does it support Logitech's PlayStation 3 Bluetooth Harmony adapter or the PS3 Bluetooth Blu-ray remote. This omission seems to signal Sony's abandonment of the media aspirations that drove much of the PS3's basic design.

                 <br>   Players who want to game privately on their consoles might be temporarily annoyed at the absence of Bluetooth headphone audio support for the PS4 at launch.</p>
                <p>This is offset somewhat: the PS4 can broadcast all game audio (and voice chat) to the audio/mic jack on the DualShock 4, which is compatible with all headphones and mobile headsets using 1/8 inch audio adapters — though enabling game audio output on the DualShock 4 disables all audio via HDMI and optical out.

                <br> Despite these problems, Sony nails the fundamentals with the PS4 hardware. The half-gloss, half-matte finish is a pleasant visual compromise. It's a grown-up machine, designed more like a stylish DVD player than a gaudy video game console. It's a small, attractive system, and one that also happens to pack more powerful hardware in its diminutive frame than any other console.</p>                        
