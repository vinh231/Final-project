            <h1>IMPROVEMENTS</h1>
            <p>Little things stand out. You can temporarily suspend a game at any time by hitting the PlayStation button and, say, change your display or audio settings without quitting the game in question. After seven or eight years of the Xbox 360 and PS3, navigating the PS4's menus and UI quickly and with little lag is one of the more refreshing elements of the next-gen console experience.</p>                    
                        
            <p>One major, appreciated change: the bifurcated User/PSN ID system of the PS3 is gone. You can now skip the PS3's user ID/PSN ID association process and directly download your profile onto the PS4. The PS4 also supports greatly improved user switching, and its guest feature allows you to temporarily download a PSN profile onto a friend's PS4 and delete it when logged out. Other small touches delight — like double-tapping the PlayStation button to toggle between active apps and games.</p>

            <p>The legacy of the Playstation 3's operating system remains in the PS4's often confusing hierarchy of menus, sub-menus and hidden options. For example, the console's Notifications menu includes Notifications, Invitations, Game Alerts, Downloads and Uploads, all as distinct inboxes. The Friends menu includes separate inboxes for Friend Requests and Name Requests, both of which also appear under Notifications. Perhaps the utility of these discrete areas will become more clear with more time spent using the system.</p>

