        <h1>OPERATING SYSTEM</h1>
        <h2> USER INTERFACE & EXPERIENCE</h2>
        <p>At first glance, it may appear that the PS4's user interface shares little in common with the PlayStation 3's "XrossMediaBar" interface, a familiar sight for owners of Sony's 2006-era televisions. But the PS4's interface is again similar to Sony's current televisions and with use, a clear evolution of the XMB is apparent. It retains its predecessor's speed, while adding a flexibility that the rigid XMB hierarchy never allowed.</p>
        <img src="images/ps4sr.jpg" alt="">
    
    
    </div>





